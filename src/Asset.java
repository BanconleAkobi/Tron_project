public class Asset {
    public String name; 
    public int price;
    
    /**
     * Constructeur de la classe Asset.
     * 
     * @param name  Nom de l'actif (asset).
     * @param price Prix de l'actif (asset).
     */
    public Asset(String name, int price) {
        this.name = name;
        this.price = price;
    }

}
